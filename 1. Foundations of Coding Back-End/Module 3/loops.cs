// WHILE LOOPS
// Executes an action if the condition is true
int counter = 0;

while (counter < 10)
{
    Console.WriteLine(counter);
    counter++;
}

// Do while. Executes an action at least once before verify if the condition is true
int counter2 = 0;

do
{
    Console.WriteLine(counter2);
    counter2++;
} while (counter2 < 10);


for (int i = 5; i > 0; i--)
{
    Console.WriteLine(i);
}